    const orderData = {
      productId,
      productName,
      customerId,
      customerName,
      customerContact,
      deliveryDate,
      packaging,
      deliveryMethod,
      additionalComment
    };

    const savedOrder = await googleSheetsService.saveOrder(orderData);

    // –û—Ç–ø—Ä–∞–≤–∫–∞ —É–≤–µ–¥–æ–º–ª–µ–Ω–∏—è –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä—É
